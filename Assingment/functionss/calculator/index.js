function sumOfTwoNumber(a,b){
    return a+b;
}
function subOfTwoNumber(a,b){
    return a-b;
}
function mulOfTwoNumber(a,b){
    return a*b;
}
function divOfTwoNumber(a,b){
    return a/b;
}
function modOfTwoNumber(a,b){
    return a%b;
}


console.log(sumOfTwoNumber(10,10));
console.log(subOfTwoNumber(3,100));
console.log(mulOfTwoNumber(10,30));
console.log(divOfTwoNumber(70,10));
console.log(modOfTwoNumber(16,10));



